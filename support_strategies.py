BRANCH_SUPPORT = {
    "PANIC": PANIC_SUPPORT,
    "STRESS": STRESS_SUPPORT,
    "DEPRESSION": DEPRESSION_SUPPORT,
    "RELATIONSHIP": RELATIONSHIP_SUPPORT,
    "LONELINESS": LONELINESS_SUPPORT,
    "STUDY_WORK": STUDY_WORK_SUPPORT,
    "UNCLEAR": LOW_SUPPORT,
}


def get_low_support() -> str:
    return random.choice(LOW_SUPPORT)["text"]


def get_medium_support() -> str:
    return random.choice(MEDIUM_SUPPORT)["text"]


def get_additional_support(branch: str, used_types: list[str] | None = None) -> tuple[str, str]:
    used_types = used_types or []

    strategies = BRANCH_SUPPORT.get(branch, LOW_SUPPORT)

    available = [
        strategy for strategy in strategies
        if strategy["type"] not in used_types
    ]

    if not available:
        available = strategies

    strategy = random.choice(available)

    return strategy["text"], strategy["type"]
